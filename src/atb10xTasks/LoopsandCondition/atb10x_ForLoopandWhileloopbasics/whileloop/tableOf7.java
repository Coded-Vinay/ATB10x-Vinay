package atb10xTasks.LoopsandCondition.atb10x_ForLoopandWhileloopbasics.whileloop;

public class tableOf7 {
    public static void main(String[] args) {
        int i = 1;
        int p = 0;
        while (i <= 10) {
            p=7*i;
            System.out.println( " 7 X " + i + " = " + p );
            i++;
        }
    }
}
