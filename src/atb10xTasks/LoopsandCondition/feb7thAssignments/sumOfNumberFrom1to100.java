package atb10xTasks.LoopsandCondition.feb7thAssignments;

public class sumOfNumberFrom1to100 {

    public static void main(String[] args) {
        int sum = 0;
        for (int i = 1; i <= 100; i++) {

            sum+= i;

        }
        System.out.println("Sum of 10 Natural number is -->> " + sum);
    }
}
